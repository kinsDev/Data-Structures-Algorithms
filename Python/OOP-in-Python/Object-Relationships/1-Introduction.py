# While inheritance represents a relationship between classes, 
# there are situations where there are relationships between objects.

# There are three main class relationships we need to know:
# IS A (Inheritance)
# Part-of
# Has-a


# Part-of
# In this relationship, one class object is a component of another class object. 
# Given two classes, class A and class B, they are in a part-of relation if a class A object
# is a part of class B object, or vice-versa.


